import io from "socket.io-client";
import constants from "./constants";

export enum SOCKET_ACTIONS {
  CONNNECTED = "Connected",
  DISCONNECTED = "DisConnected",
  RECONNECTED = "ReConnected",
  RECONNECTATTEMPT = "ReConnectAttempt",
  ERROR = "Error",
  AUTORESPONSE = "AutoResponse",
}

export class SocketService {
  private static instance: SocketService | null = null;
  private socket: any;
  private intervalId: any;
  private currentIndex = 0;

  private constructor() {
    console.log("Construction Called ------");

    // this.setupSocketEvents()
  }

  static getInstance(): SocketService {
    if (!SocketService.instance) {
      SocketService.instance = new SocketService();
    }

    return SocketService.instance;
  }

  private setupSocketEvents(
    callback: (action: SOCKET_ACTIONS, data: any) => void
  ) {
    this.socket.on("connect", () => {
      console.log("Connected to server");
      // this.socket.emit('update-live-data', {
      //   providerId: 408,
      //   latitude: 40.7128,
      //   longitude: -74.006,
      // });
      // }, 2000);

      if (this.intervalId) {
        clearInterval(this.intervalId);
      }
      this.intervalId = setInterval(() => {
        // Increment the index by 2 (to get every 2nd position value)
        // this.currentIndex = sampleGoogleLocations().length - this.currentIndex;
        this.currentIndex += 10;
        const positions = sampleGoogleLocations();
        console.log(
          "Value --- ",
          this.currentIndex,
          sampleGoogleLocations().length
        );
        if (this.currentIndex >= sampleGoogleLocations().length) {
          clearInterval(this.intervalId);
          this.intervalId = null;
          this.currentIndex = 0;
          return;
        }
        callback(SOCKET_ACTIONS.AUTORESPONSE, positions[this.currentIndex]);
      }, 1000);

      callback(SOCKET_ACTIONS.CONNNECTED, {});
    });

    this.socket.on("disconnect", (reason: any) => {
      console.log("Disconnected from server:", reason);
      clearInterval(this.intervalId);
      this.intervalId = null;
      this.currentIndex = 0;
      callback(SOCKET_ACTIONS.DISCONNECTED, {});
    });

    this.socket.on("reconnect", (attemptNumber: any) => {
      console.log("Reconnected to server. Attempt:", attemptNumber);
      callback(SOCKET_ACTIONS.RECONNECTED, attemptNumber);
    });

    this.socket.on("reconnect_attempt", () => {
      console.log("Attempting to reconnect to server...");
      callback(SOCKET_ACTIONS.RECONNECTATTEMPT, {});
    });

    this.socket.on("error", (error: any) => {
      console.error("Socket error:", error);
      callback(SOCKET_ACTIONS.ERROR, error);
    });

    this.socket.on("live-data-updated", (data: any) => {
      console.log("Received Auto Update:", data);
      callback(SOCKET_ACTIONS.AUTORESPONSE, data);
    });
  }

  public connect(callback: (action: SOCKET_ACTIONS, data: any) => void) {
    this.socket = io(constants.SOCKET_URL);
    this.socket.connect();
    this.setupSocketEvents(callback);
  }

  public disConnectSocket() {
    this.socket.disconnect();
    this.socket = null;
  }
}

